var opr = prompt ("Please enter the Operator you want to perform \n e.g +, -, *,/ ")
var num1 = parseInt (prompt ("First Number"))
var num2 = parseInt (prompt ("Second Number"))

if (opr == "+")
{
    prompt ("The result is " (num1 + num2))

} else if (opr == "-")
{   
    prompt (num1-num2) 

} else if (opr == "*")
{   
    prompt (num1*num2)

} else if (opr == "/")
{   
    prompt (num1/num2)

}

